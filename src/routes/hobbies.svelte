<h2>Twitch</h2>
<p>
I recently started an academic talk series on Twitch, called <a href="https://www.twitch.tv/thinkswithtwitch">ThinksWithTwitch</a>. Each week we bring on a speaker to give a talk about their research followed by a Q&amp;A. We source talks from a variety of topics across STEM, including technology, healthcare, machine learning, physics, and biology. 
</p>

<p>
If you're interested in science I'd recommend you check out a stream and ask questions in the chat! Past talks are also available on <a href="https://www.youtube.com/channel/UCsYlWeIYhLh5tGdL-c9MmNQ">our YouTube channel</a>. We're also constantly looking for new speakers, so if you've done some work in an interesting area and can communicate your work to a scientifically literate outside of your domain, please contact me!
</p>

<h2>FDLG</h2>
<p>
During quarantine my friend and I created <a href="https://fdlg.herokuapp.com/">Furthest Distance Lunch Groups</a>, coordinated Zoom lunches for students. Students sign up and each week we pair them with 3 other random students to virtually meet up for lunch over Zoom. We started in UC Berkeley, but soon expanded to nearly 400 students from CMU, UW, Georgia Tech, UPenn, Princeton, Cornell, UIUC, UChicago, NYU, and others. Our goal was to provide more social interaction for people stuck at home as well as an opportunity to meet new people with different interests. It was good opportunity to learn Javascript and Node.js, and to use technology for some good. 
</p>

<p>
I have had to step away from FDLG for the time being as other tasks have taken priority. However I've left it in capable hands. 
</p>
